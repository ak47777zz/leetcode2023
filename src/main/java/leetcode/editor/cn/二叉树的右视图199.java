        public List<Integer> rightSideView(TreeNode root) {
            List<Integer> res = new ArrayList<>();
            if (root == null) {
                return res;
            }
            Queue<TreeNode> queue = new LinkedList<>();
            queue.add(root);
            while (!queue.isEmpty()) {
                int size = queue.size();
                for (int i = 0; i < size; i++) {
                    TreeNode node = queue.poll();
                    if (node.left != null) {
                        queue.add(node.left);
                    }
                    if (node.right != null) {
                        queue.add(node.right);
                    }
                    // éå†åˆ°è¿™ä¸€å±‚æœ€åä¸€ä¸ªèŠ‚ç‚¹æ—¶,ä¿å­˜å½“å‰å€¼
                    if (i == size - 1) {
                        res.add(node.val);
                    }
                }
            }
            return res;
        }
