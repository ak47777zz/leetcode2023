    class Solution {
        public boolean isBalanced(TreeNode root) {
            return deep(root) != -1;
        }

        private int deep(TreeNode root) {
            if (root == null) {
                return 0;
            }
            int leftDeep = deep(root.left);
            if (leftDeep == -1) {
                return -1;
            }
            int rightDeep = deep(root.right);
            if (rightDeep == -1) {
                return -1;
            }

            if (Math.abs(leftDeep - rightDeep) > 1) {
                return -1;
            }

            return Math.max(leftDeep, rightDeep) + 1;
        }
    }
