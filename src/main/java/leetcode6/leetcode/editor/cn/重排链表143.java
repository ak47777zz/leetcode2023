    class Solution {
        public void reorderList(ListNode head) {
            Deque<ListNode> deque = new LinkedList<>();
            ListNode cur = head.next;
            while (cur != null) {
                deque.add(cur);
                cur = cur.next;
            }

            cur = head;
            int count = 0;
            while (!deque.isEmpty()) {
                // å¶æ•°å–é˜Ÿå°¾
                if (count % 2 == 0) {
                    cur.next = deque.pollLast();
                } else {
                    // å¥‡æ•°å–é˜Ÿå¤´
                    cur.next = deque.pollFirst();
                }
                cur = cur.next;
                count++;
            }
            cur.next = null;
        }
    }
